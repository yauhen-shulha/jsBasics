// === JavaScript Number Methods for QA Automation ===

/* 1. Convert string to number */
// Method: Number()
const str1 = "42";
// Output: 42

/* 2. Parse integer from a string */
// Method: parseInt()
const str2 = "123px";
// Output: 123

/* 3. Parse float from a string */
// Method: parseFloat()
const str3 = "3.14 meters";
// Output: 3.14

/* 4. Round number to nearest integer */
// Method: Math.round()
const value1 = 5.6;
// Output: 6

/* 5. Round number down */
// Method: Math.floor()
const value2 = 7.9;
// Output: 7

/* 6. Round number up */
// Method: Math.ceil()
const value3 = 4.1;
// Output: 5

/* 7. Limit decimal places */
// Method: .toFixed()
const num1 = 3.14159;
// Output: "3.14"

/* 8. Get absolute value */
// Method: Math.abs()
const value4 = -50;
// Output: 50

/* 9. Check if a value is NaN */
// Method: isNaN()
const result = NaN;
// Output: true

/* 10. Check if a value is finite */
// Method: isFinite()
const test = 999;
// Output: true
